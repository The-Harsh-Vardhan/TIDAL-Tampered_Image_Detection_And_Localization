    "# ── Validation function ──────────────────────────────────────────────\n",
    "@torch.no_grad()\n",
    "def validate(model, val_loader, criterion, device, threshold=0.5):\n",
    "    model.eval()\n",
    "    total_loss = 0.0\n",
    "    f1_scores = []\n",
    "    iou_scores = []\n",
    "\n",
    "    for images, masks, labels in val_loader:\n",
    "        images = images.to(device)\n",
