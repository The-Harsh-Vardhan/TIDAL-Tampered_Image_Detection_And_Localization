    "        with autocast('cuda'):\n",
    "            logits = model(images)\n",
    "            loss = criterion(logits, masks)\n",
    "\n",
    "        total_loss += loss.item()\n",
    "\n",
    "        probs = torch.sigmoid(logits)\n",
    "        preds = (probs > threshold).float()\n",
    "\n",
    "        for pred, gt in zip(preds, masks):\n",
    "            f1_scores.append(compute_pixel_f1(pred, gt))\n",
    "            iou_scores.append(compute_iou(pred, gt))\n",
    "\n",
    "    avg_loss = total_loss / len(val_loader)\n",
    "    avg_f1 = np.mean(f1_scores)\n",
    "    avg_iou = np.mean(iou_scores)\n",
    "    return avg_loss, avg_f1, avg_iou"
