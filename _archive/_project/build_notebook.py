cells.append(code([
    "# ── Metric functions ─────────────────────────────────────────────────\n",
    "def compute_pixel_f1(pred, gt, eps=1e-8):\n",
    "    \"\"\"Pixel-level F1 score for a single image.\"\"\"\n",
    "    pred = pred.flatten()\n",
    "    gt = gt.flatten()\n",
    "\n",
    "    # Handle authentic images (empty masks)\n",
    "    if gt.sum() == 0 and pred.sum() == 0:\n",
    "        return 1.0\n",
    "    if gt.sum() == 0 and pred.sum() > 0:\n",
    "        return 0.0\n",
    "\n",
    "    tp = (pred * gt).sum()\n",
    "    fp = (pred * (1 - gt)).sum()\n",
    "    fn = ((1 - pred) * gt).sum()\n",
    "\n",
    "    precision = tp / (tp + fp + eps)\n",
    "    recall = tp / (tp + fn + eps)\n",
    "    f1 = 2 * precision * recall / (precision + recall + eps)\n",
    "    return f1.item()\n",
    "\n",
    "\n",
    "def compute_iou(pred, gt, eps=1e-8):\n",
    "    \"\"\"Pixel-level IoU for a single image.\"\"\"\n",
    "    pred = pred.flatten()\n",
    "    gt = gt.flatten()\n",
    "\n",
    "    if gt.sum() == 0 and pred.sum() == 0:\n",
    "        return 1.0\n",
    "\n",
    "    intersection = (pred * gt).sum()\n",
    "    union = pred.sum() + gt.sum() - intersection\n",
    "    if union == 0:\n",
    "        return 1.0\n",
    "    return (intersection / (union + eps)).item()\n",
    "\n",
    "\n",
    "def compute_precision_recall(pred, gt, eps=1e-8):\n",
    "    \"\"\"Pixel-level precision and recall.\"\"\"\n",
    "    pred = pred.flatten()\n",
    "    gt = gt.flatten()\n",
    "\n",
    "    tp = (pred * gt).sum()\n",
    "    fp = (pred * (1 - gt)).sum()\n",
    "    fn = ((1 - pred) * gt).sum()\n",
    "\n",
    "    precision = (tp / (tp + fp + eps)).item()\n",
    "    recall = (tp / (tp + fn + eps)).item()\n",
    "    return precision, recall\n",
    "\n",
    "\n",
    "def compute_image_accuracy(scores, labels, threshold=0.5):\n",
    "    preds = (np.array(scores) >= threshold).astype(int)\n",
    "    return (preds == np.array(labels)).mean()\n",
    "\n",
    "\n",
    "def compute_image_auc(scores, labels):\n",
    "    return roc_auc_score(labels, scores)\n",
    "\n",
    "\n",
    "print(\"Metrics defined: Pixel-F1, IoU, Precision, Recall, Image Accuracy, Image AUC-ROC\")"
]))

