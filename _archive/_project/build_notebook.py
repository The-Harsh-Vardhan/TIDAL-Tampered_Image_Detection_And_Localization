    "    {'params': model.encoder.parameters(),          'lr': CONFIG['encoder_lr']},\n",
    "    {'params': model.decoder.parameters(),          'lr': CONFIG['decoder_lr']},\n",
    "    {'params': model.segmentation_head.parameters(), 'lr': CONFIG['decoder_lr']},\n",
    "], weight_decay=CONFIG['weight_decay'])\n",
    "\n",
    "# AMP scaler\n",
    "scaler = GradScaler('cuda')\n",
    "\n",
    "print(f\"Optimizer: AdamW\")\n",
    "print(f\"  Encoder LR: {CONFIG['encoder_lr']}\")\n",
    "print(f\"  Decoder LR: {CONFIG['decoder_lr']}\")\n",
    "print(f\"  Weight decay: {CONFIG['weight_decay']}\")\n",
